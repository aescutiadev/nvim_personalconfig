return {
  'lewis6991/gitsigns.nvim',
  event = { 'BufReadPre', 'BufNewFile' },
  opts = {
    signs = {
      add = { text = '│' },
      change = { text = '│' },
      delete = { text = '_' },
      topdelete = { text = '‾' },
      changedelete = { text = '~' },
      untracked = { text = '┆' },
    },
    on_attach = function(bufnr)
      local gitsigns = require('gitsigns')

      local function map(mode, lhs, rhs, opts)
        opts = opts or {}
        opts.buffer = bufnr
        vim.keymap.set(mode, lhs, rhs, opts)
      end

      -- Navigation
      map('n', ']c', function()
        if vim.wo.diff then
          vim.cmd.normal({ ']c', bang = true })
        else
          gitsigns.nav_hunk('next')
        end
      end, { desc = 'Jump to next git [c]hange' })

      map('n', '[c', function()
        if vim.wo.diff then
          vim.cmd.normal({ '[c', bang = true })
        else
          gitsigns.nav_hunk('prev')
        end
      end, { desc = 'Jump to previous git [c]hange' })

      -- Actions - Visual mode
      map('v', '<leader>ghs', function()
        gitsigns.stage_hunk({ vim.fn.line('.'), vim.fn.line('v') })
      end, { desc = 'Git [s]tage hunk' })

      map('v', '<leader>ghr', function()
        gitsigns.reset_hunk({ vim.fn.line('.'), vim.fn.line('v') })
      end, { desc = 'Git [r]eset hunk' })

      -- Actions - Normal mode
      map('n', '<leader>ghS', gitsigns.stage_buffer, { desc = 'Git [S]tage buffer' })
      map('n', '<leader>ghu', gitsigns.undo_stage_hunk, { desc = 'Git [u]ndo stage hunk' })
      map('n', '<leader>ghR', gitsigns.reset_buffer, { desc = 'Git [R]eset buffer' })
      map('n', '<leader>ghp', gitsigns.preview_hunk, { desc = 'Git [p]review hunk' })
      map('n', '<leader>ghb', gitsigns.blame_line, { desc = 'Git [b]lame line' })
      map('n', '<leader>ghd', gitsigns.diffthis, { desc = 'Git [d]iff against index' })
      map('n', '<leader>ghD', function()
        gitsigns.diffthis('@')
      end, { desc = 'Git [D]iff against last commit' })

      -- Toggles
      map('n', '<leader>ght', gitsigns.toggle_current_line_blame, { desc = '[T]oggle git blame line' })
      map('n', '<leader>ghT', gitsigns.toggle_deleted, { desc = '[T]oggle git show deleted' })
    end,
  },
}
